package com.tnsif.Day2;

public class LoopingStatement 
{

	public static void main(String[] args) 
	{
		int  i=10;
		while(i<=15)
		{
			System.out.println(i);
			i++;
		}
	}

}
/*
 * Looping statements: 
 * ==================
 * 
 * repeat
 * 4types:
 * for,while,do while ,for each
 * 3parts of loop:
 * Initialization =Starting range
 * Condition =Ending range
 * Inc/Dec
 * 
 * for Syntax:
 * ==========
 * for(initialization(1);condition(2);inc/dec(4))
 * {
 * 	body (3)
 * }
 * 
 * 
 * 
 * while Syntax:
 * =============
 * initialization (1)
 * while(condition)(2)
 * {
 * 	body
 * 	Inc/dec(3)
 * }
 * 
 * do while:
 * ========
 * Initialzation(1)
 * do
 * {
 * 	Body(2)
 * 	Inc/Dec(4)
 * }While(condition);(3)
 * 
 * 
 * 
 * 		int a;
		for(a=10;a<=20;a++)
		{
			System.out.println(a);//10 11
		}

 */
